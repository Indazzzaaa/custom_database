# ANSI escape codes for text colors
RED = '\033[31m'
GREEN = '\033[32m'
YELLOW = '\033[33m'
BLUE = '\033[34m'
MAGENTA = '\033[35m'
CYAN = '\033[36m'
RESET = '\033[0m'

# Print colored text
print(f"{RED}This line is red.{RESET}")
print(f"{GREEN}This line is green.{RESET}")
print(f"{YELLOW}This line is yellow.{RESET}")
print(f"{BLUE}This line is blue.{RESET}")
print(f"{MAGENTA}This line is magenta.{RESET}")
print(f"{CYAN}This line is cyan.{RESET}")
