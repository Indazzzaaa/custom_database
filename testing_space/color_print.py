# ANSI escape codes for text colors
RED = '\033[31m'
GREEN = '\033[32m'
YELLOW = '\033[33m'
BLUE = '\033[34m'
MAGENTA = '\033[35m'
CYAN = '\033[36m'
RESET = '\033[0m'

# Print colored text
print(f"{RED}Error: {RESET}Something went wrong!")
print(f"{GREEN}Success: {RESET}Operation completed successfully.")